    {
        private Estrato _Estrato;
        private decimal _ValorServicio;
        private UnidadTarifa _Unidad;
        private short _IdServicio;

        public Estrato Estrato
        {
            get
            {
                return _Estrato;
            }

            set
            {
                _Estrato = value;
            }
        }

        public decimal ValorServicio
        {
            get
            {
                return _ValorServicio;
            }

            set
            {
                _ValorServicio = value;
            }
        }

        public UnidadTarifa Unidad
        {
            get
            {
                return _Unidad;
            }

            set
            {
                _Unidad = value;
            }
        }

        public short IdServicio
        {
            get
            {
                return _IdServicio;
            }

            set
            {
                _IdServicio = value;
            }
        }
    }
}
